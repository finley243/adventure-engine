package com.github.finley243.adventureengine.world.template;

public class StatsItem {

	private String ID;
	private String name;
	
	public StatsItem(String ID, String name) {
		this.ID = ID;
		this.name = name;
	}
	
	public String getID() {
		return ID;
	}
	
	public String getName() {
		return name;
	}
	
}
